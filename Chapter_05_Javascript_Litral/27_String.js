// String quotes
let single = 'Hell world';
let withDouble = 'She said, "Hello!"';

// Double quotes
let double = "Hello world";
let withSingle = "It's a nice day!";

let c = 'c';

console.log(typeof single); // "string"
console.log(typeof withDouble); // "string"
console.log(typeof double); // "string"
console.log(typeof withSingle); // "string"
console.log(typeof c); // "string"
console.log(typeof double); // "H"


